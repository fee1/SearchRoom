package com.zxf.DTO;

public class BaiduMapLocation {

    // 经度
    private double lon;

    // 纬度
    private double lat;

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }
}
